int main()
{
    auto obj = getPenger();
    Renderer renderer;

    std::unique_ptr<IPlatform> gfx =
        std::make_unique<SDLPlatform>();

    if (!gfx->initialize(800, 600, "Software Renderer"))
        return -1;

    using clock = std::chrono::steady_clock;

    auto lastTime = clock::now();

    bool running = true;
    while (running)
    {
        auto frameStart = clock::now();

        // ---- Time step ----
        std::chrono::duration<double> delta =
            frameStart - lastTime;
        lastTime = frameStart;

        double deltaTime = delta.count();

        // Clamp deltaTime (prevents huge jumps if debugger pauses)
        if (deltaTime > 0.25)
            deltaTime = 0.25;

        // ---- Events ----
        running = gfx->processEvents();

        // ---- Update ----
        rotateObj(obj, M_PI * deltaTime / 10);

        // ---- Render ----
        Framebuffer& fb = gfx->framebuffer();

        fb.clear(0xFF335588);
        renderer.render(fb, obj);
        gfx->present();

        // ---- Frame pacing ----
        auto frameEnd = clock::now();
        std::chrono::duration<double> frameDuration =
            frameEnd - frameStart;

        if (frameDuration.count() < TARGET_FRAME_TIME)
        {
            std::this_thread::sleep_for(
                std::chrono::duration<double>(
                    TARGET_FRAME_TIME - frameDuration.count()
                )
            );
        }
    }

    gfx->shutdown();
    return 0;
}

